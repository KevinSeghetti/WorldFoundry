print DEP "TEXTILE = "."$bindir"."textile\n";
print DEP "IFFCOMP = "."$bindir"."iffcomp\n";
print DEP "IFF2LVL = "."$bindir"."iff2lvl\n";
print DEP "PREP = "."$bindir"."prep\n";
