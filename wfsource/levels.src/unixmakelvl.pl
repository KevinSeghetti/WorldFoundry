print DEP "\t-rm $level_asset_dir/*.map\n";
print DEP "\t-rm $level_asset_dir/*.wav\n";
print DEP "\t-rm $level_asset_dir/*.tga\n";
print DEP "\t-rm $level_asset_dir/*.ruv\n";
