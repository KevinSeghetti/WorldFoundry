	$result = /(^$level_dir)(\.max|\.i3d)$/i;
	#print "\$_=[$_] \$1=[$1] \$2=[$2]\n";

	if ( $result )
	{
		$level_name = $1;
		print "FOUND LEVEL: \$_=[$_] \$1=[$1] \$2=[$2] \$level_dir=[$level_dir]\n";

		&add_dir( $level_asset_dir );

