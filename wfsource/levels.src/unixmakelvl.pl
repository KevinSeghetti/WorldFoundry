print DEP "\t-rmdir $level_asset_dir\n";
print DEP "\n";

print DEP $line_separator;
print DEP "\nCD : \n";
print DEP "\tcd .. ;";
print DEP "\ttouch levels.txt ;";
print DEP "\tmake cd\n";
print DEP "\n";

close DEP;


